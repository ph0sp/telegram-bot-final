# Вспомогательные функции для работы с вопросами
def get_question_block(question_index: int) -> str:
    """Возвращает название блока для вопроса по индексу"""
    if 0 <= question_index < len(QUESTIONS):
        return QUESTIONS[question_index]["block"]
    return "Unknown Block"

def get_question_text(question_index: int) -> str:
    """Возвращает текст вопроса по индексу"""
    if 0 <= question_index < len(QUESTIONS):
        return QUESTIONS[question_index]["text"]
    return "Question not found"

def get_total_questions() -> int:
    """Возвращает общее количество вопросов"""
    return len(QUESTIONS)

def validate_questions_structure() -> bool:
    """Проверяет структуру вопросов на корректность"""
    required_keys = {"block", "text"}
    
    for i, question in enumerate(QUESTIONS):
        if not all(key in question for key in required_keys):
            print(f"❌ Question {i} missing required keys: {question}")
            return False
            
        if not isinstance(question["block"], str) or not isinstance(question["text"], str):
            print(f"❌ Question {i} has invalid data types")
            return False
    
    print(f"✅ All {len(QUESTIONS)} questions validated successfully")
    return True

# Валидация при импорте
if __name__ == "__main__":
    validate_questions_structure()
else:
    validate_questions_structure()
